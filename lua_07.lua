-->>> Lua 字符串

--字符串或者串（String）是由数字、字母、下划线组成的一串字符。
--Lua语言中字符串可以使用一下三种方式来表示：
--A.单引号的一串字符
--B.双引号的一串字符
--C.[[]]之间的一串字符
--示例如下：
-- str1='Hello World 1';
-- str2="Hello World 2";
-- str3=[[Hell World 3]];
-- print(str1,str2,str3);

--1.字符串转义字符
--转义字符用于表示不能直接显示的字符，比如后退键，回车键等
--Lua中所有的转义字符如下：
--\a    响铃（BEL）
--\b    退格（BS）,将当前位置移动前一列
--\f    换页（FF）,将当前位置移到下页开头
--\n    换行（LF）,将当前位置移到下一行开头
--\r    回车（CR），将当前位置移到本行开头
--\t    水平制表（HT)，跳到下一个TAB位置
--\v    垂直制表（VT），
--\\    代表一个反斜线字符“\”
--\`    代表一个单撇号字符
--\"    代表一个双引号
--\0    代表一个空字符（Null）
--\ddd  代表1到3位八进制所代表的任意字符
--\xhh  代表1到2位十六进制所代表的任意字符

